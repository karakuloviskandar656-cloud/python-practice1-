# Different data types in Python

# String
name = "Iskandar"
print("String:", name)

# Integer
age = 20
print("Integer:", age)

# Float
height = 1.75
print("Float:", height)

# Boolean
is_student = True
print("Boolean:", is_student)

# List
fruits = ["apple", "banana", "cherry"]
print("List:", fruits)

# Tuple
coordinates = (10, 20)
print("Tuple:", coordinates)

# Dictionary
person = {"name": "Iskandar", "age": 20}
print("Dictionary:", person)
